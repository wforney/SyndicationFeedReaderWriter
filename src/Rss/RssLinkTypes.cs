namespace Microsoft.SyndicationFeed.Rss;

/// <summary>
/// Provides constant values for common RSS link types.
/// </summary>
public static class RssLinkTypes
{
    /// <summary>
    /// Represents an alternate link type.
    /// </summary>
    public const string Alternate = "alternate";

    /// <summary>
    /// Represents a comments link type.
    /// </summary>
    public const string Comments = "comments";

    /// <summary>
    /// Represents an enclosure link type.
    /// </summary>
    public const string Enclosure = "enclosure";

    /// <summary>
    /// Represents a GUID link type.
    /// </summary>
    public const string Guid = "guid";

    /// <summary>
    /// Represents a source link type.
    /// </summary>
    public const string Source = "source";
}