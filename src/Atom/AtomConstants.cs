namespace Microsoft.SyndicationFeed.Atom;

/// <summary>
/// Defines constants used in Atom syndication feeds.
/// </summary>
internal static class AtomConstants
{
    /// <summary>
    /// The XML namespace for Atom 1.0.
    /// </summary>
    public const string Atom10Namespace = "http://www.w3.org/2005/Atom";

    /// <summary>
    /// The XML prefix commonly used for Atom 1.0 elements.
    /// </summary>
    public const string Atom10Prefix = "a10";

    /// <summary>
    /// The 'href' attribute name.
    /// </summary>
    public const string Href = "href";

    /// <summary>
    /// The 'label' attribute name.
    /// </summary>
    public const string Label = "label";

    /// <summary>
    /// The 'length' attribute name.
    /// </summary>
    public const string Length = "length";

    /// <summary>
    /// The content type for plain text.
    /// </summary>
    public const string PlainTextContentType = "text";

    /// <summary>
    /// The 'rel' attribute name.
    /// </summary>
    public const string Rel = "rel";

    /// <summary>
    /// The 'scheme' attribute name.
    /// </summary>
    public const string Scheme = "scheme";

    /// <summary>
    /// The 'src' attribute name.
    /// </summary>
    public const string Source = "src";

    /// <summary>
    /// The link to the Atom format specification.
    /// </summary>
    public const string SpecificationLink = "http://atompub.org/2005/08/17/draft-ietf-atompub-format-11.html";

    /// <summary>
    /// The 'term' attribute name.
    /// </summary>
    public const string Term = "term";

    /// <summary>
    /// The 'type' attribute name.
    /// </summary>
    public const string Type = "type";

    /// <summary>
    /// The 'uri' attribute name.
    /// </summary>
    public const string Uri = "uri";

    /// <summary>
    /// The 'version' attribute name.
    /// </summary>
    public const string Version = "version";

    /// <summary>
    /// The content type for XHTML.
    /// </summary>
    public const string XhtmlContentType = "xhtml";

    /// <summary>
    /// The XML namespace for XHTML.
    /// </summary>
    public const string XhtmlNamespace = "http://www.w3.org/1999/xhtml";
}
