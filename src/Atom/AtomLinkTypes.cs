namespace Microsoft.SyndicationFeed.Atom;

/// <summary>
/// Provides constants for the standard link types used in Atom syndication feeds.
/// </summary>
public static class AtomLinkTypes
{
    /// <summary>
    /// The "alternate" link type.
    /// </summary>
    public const string Alternate = "alternate";

    /// <summary>
    /// The "content" link type.
    /// </summary>
    public const string Content = "content";

    /// <summary>
    /// The "enclosure" link type.
    /// </summary>
    public const string Enclosure = "enclosure";

    /// <summary>
    /// The "related" link type.
    /// </summary>
    public const string Related = "related";

    /// <summary>
    /// The "self" link type.
    /// </summary>
    public const string Self = "self";

    /// <summary>
    /// The "source" link type.
    /// </summary>
    public const string Source = "source";
}